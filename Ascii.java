public class Ascii{
    public static void main(String[] args)
    {
        char s1='a';
        char s2='b';
        
        int ans1=s1;
        int ans2=s2;
        
        System.out.println("The ascii value of "+s1 +" is: "+ans1);
        System.out.println("The ascii value of "+s2 +" is: "+ans2);
        
    }
}
